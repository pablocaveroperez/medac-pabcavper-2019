package Problema1;

public class Fecha {
    private byte bDia;
    private byte bMes;
    private short bAnio;

    public Fecha(){

    }

    public byte getbDia() {
        return bDia;
    }

    public void setbDia(byte bDia) {
        this.bDia = bDia;
    }

    public byte getbMes() {
        return bMes;
    }

    public void setbMes(byte bMes) {
        this.bMes = bMes;
    }

    public short getbAnio() {
        return bAnio;
    }

    public void setbAnio(short bAnio) {
        this.bAnio = bAnio;
    }
}
